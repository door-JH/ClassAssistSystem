package info.example.beans;

public class ReportContentsBean {
	
	private int report_contents_idx;
	private String report_contents_subject;
	private String report_contents_text;
	private int report_contents_writer_idx;
	private int report_contents_info_idx;
	private String report_contents_date;
	
	public int getReport_contents_idx() {
		return report_contents_idx;
	}
	public void setReport_contents_idx(int report_contents_idx) {
		this.report_contents_idx = report_contents_idx;
	}
	public String getReport_contents_subject() {
		return report_contents_subject;
	}
	public void setReport_contents_subject(String report_contents_subject) {
		this.report_contents_subject = report_contents_subject;
	}
	public String getReport_contents_text() {
		return report_contents_text;
	}
	public void setReport_contents_text(String report_contents_text) {
		this.report_contents_text = report_contents_text;
	}
	public int getReport_contents_writer_idx() {
		return report_contents_writer_idx;
	}
	public void setReport_contents_writer_idx(int report_contents_writer_idx) {
		this.report_contents_writer_idx = report_contents_writer_idx;
	}
	public int getReport_contents_info_idx() {
		return report_contents_info_idx;
	}
	public void setReport_contents_info_idx(int report_contents_info_idx) {
		this.report_contents_info_idx = report_contents_info_idx;
	}
	public String getReport_contents_date() {
		return report_contents_date;
	}
	public void setReport_contents_date(String report_contents_date) {
		this.report_contents_date = report_contents_date;
	}

}
