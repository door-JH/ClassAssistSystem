package info.example.beans;

public class ClassInfoBean {
	
	private int class_info_idx;
	private String class_info_year;
	private String class_info_semester;
	private String class_info_name;
	
	public int getClass_info_idx() {
		return class_info_idx;
	}
	public void setClass_info_idx(int class_info_idx) {
		this.class_info_idx = class_info_idx;
	}
	public String getClass_info_year() {
		return class_info_year;
	}
	public void setClass_info_year(String class_info_year) {
		this.class_info_year = class_info_year;
	}
	public String getClass_info_semester() {
		return class_info_semester;
	}
	public void setClass_info_semester(String class_info_semester) {
		this.class_info_semester = class_info_semester;
	}
	public String getClass_info_name() {
		return class_info_name;
	}
	public void setClass_info_name(String class_info_name) {
		this.class_info_name = class_info_name;
	}

}
